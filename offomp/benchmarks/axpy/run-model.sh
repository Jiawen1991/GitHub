#!/bin/bash
allsizes="100000 200000 300000 400000 500000 1000000"

export OMP_NUM_NVGPU_DEVICES=0
export OMP_NUM_THSIM_DEVICES=1
export OMP_NUM_THREADS=32
unset OMP_NVGPU_DEVICES
for size in $allsizes; do
echo "-------------------------------------------------------------------------------------------------"
echo "-------------------------------- AXPY ${size}, host run with $OMP_NUM_THREADS threads -----------------------------"
./axpy-nvgpu $size
echo "-------------------------------------------------------------------------------------------------"
echo "-------------------------------------------------------------------------------------------------"
done

export OMP_NUM_THSIM_DEVICES=0
for dev in 0 1; do
export OMP_NVGPU_DEVICES=$dev
for size in $allsizes; do
echo "-------------------------------------------------------------------------------------------------"
echo "-------------------------------- AXPY ${size}, device run on GPU $OMP_NVGPU_DEVICES -----------------------------"
./axpy-nvgpu $size
echo "-------------------------------------------------------------------------------------------------"
echo "-------------------------------------------------------------------------------------------------"
done
done
